using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;
using UnityEngine.SceneManagement;

public class MenuController : MonoBehaviour
{
    public GameObject mainMenu;
    public GameObject controlMenu;
    public GameObject settingsMenu;
    public GameObject credits;

    public GameObject names1;

    [SerializeField] AudioManager audioManager;
    
    void Start()
    {
        MainMenu();
    }

    public void MainMenu()
    {
        controlMenu.SetActive(false);
        credits.SetActive(false);
        //howToPlay.SetActive(false);
        mainMenu.SetActive(true);
        settingsMenu.SetActive(false);

    }

    public void SettingsMenu()
    {
        mainMenu.SetActive(false);
        settingsMenu.SetActive(true);
    }

    public void creditsMenu()
    {
        credits.SetActive(true);
        names1.SetActive(true);
        mainMenu.SetActive(false);
    }
    
    public void QuitGame()
    {
        // this command quits the game
        Application.Quit();
    }

    public void ControlsMenu()
    {
        // deactivate/activate the parent gameobjects for different menus
        controlMenu.SetActive(true);
        mainMenu.SetActive(false);
    }

    public void HowToPlay()
    {
        //howToPlay.SetActive(true);
        mainMenu.SetActive(false);
    }
    public void StartGame()
    {
        audioManager.ChangeMusicImmediate("Level1");
        // set the scene to the first game level
        SceneManager.LoadScene("Level1");
    }
}
